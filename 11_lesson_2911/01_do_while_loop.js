// условный цикл do...while
let count = 1;
do {
    console.log(count);
    count++
} while (count <= 10);
/*Такая форма синтаксиса оправдана,
 если вы хотите, чтобы тело цикла
 выполнилось хотя бы один раз,
 даже если условие окажется ложным.

 На практике чаще используется форма с
 предусловием: while(…) {…}.*/


