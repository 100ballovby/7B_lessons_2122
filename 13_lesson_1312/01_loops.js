for (let i = 10; i > 0; i--) {
    console.log(i)
} // вывести все четные цифры от 0 до 100 по возрастанию

for (let i = 0; i <= 100; i += 2) {
    console.log(i)
}
/*
i-- / i++ <- уменьшение/увеличение на 1
i -= x / i += x <- уменьшение/увеличение на x
i = i - x / i = i + x <- уменьшение/увеличение на x
*/


